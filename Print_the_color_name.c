#include<stdio.h>
int main()
{
    char chhichhore,V,I,B,G,Y,O,R;
    scanf("%c",&chhichhore);
    if(chhichhore==V)
    {
        printf("Violet");
    }
    else if(chhichhore=='I')
    {
        printf("Indigo");
    }
    else if(chhichhore=='B')
    {
        printf("Blue");
    }
    else if(chhichhore=='G')
    {
        printf("Green");
    }
    else if(chhichhore=='Y')
    {
        printf("Yellow");
    }
    else if(chhichhore=='O')
    {
        printf("Orange");
    }
    else if(chhichhore=='R')
    {
        printf("Red");
    }
    else 
    {
        printf("-1");
    }
}